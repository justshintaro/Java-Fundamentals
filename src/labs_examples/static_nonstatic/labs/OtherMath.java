package labs_examples.static_nonstatic.labs;

public class OtherMath {

    public static void main(String[] args) {
        Substraction(5, 2);
    }

    public static void Substraction(int a, int b){
        int c = a -b;
        System.out.println(c);
    }

    public void Division(float a, float b){
        float c = a - b;
        System.out.println(c);
    }
}
